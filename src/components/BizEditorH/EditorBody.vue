<template>
  <div class="editor-body" :style="style">
    <div :id="id"></div>
  </div>
</template>

<script>
import http from '@/common/http';
// 一个简单的事件订阅发布的实现,取代原始Event对象,提升IE下的兼容性
class LoadEvent {
  constructor() {
    this.listeners = {};
  }

  on(eventName, callback) {
    if (this.listeners[eventName] === undefined) {
      this.listeners[eventName] = [];
    }
    this.listeners[eventName].push(callback);
  }

  emit(eventName) {
    this.listeners[eventName] && this.listeners[eventName].forEach(callback => callback());
  }
}

export default {
  name: 'UeditorWrap',
  data() {
    return {
      imgList: [],
      reg: new RegExp(/<img [^>]* src=['"](http[s]*:\/\/[^'"]+)[^>]*>/g, 'i'),
      id:
        'editor' +
        Math.random()
          .toString()
          .slice(-10),
      editor: null,
      status: 0,
      initValue: '',
      defaultConfig: {
        toolbars: [],
        initialFrameWidth: null,
        // 编辑器不自动被内容撑高
        autoHeightEnabled: false,
        // 初始容器高度
        initialFrameHeight: 300,
        // 关闭自动保存
        enableAutoSave: false,
        wordCount: false,
        elementPathEnabled: false,
        scaleEnabled: false,
        UEDITOR_HOME_URL: './static/ueditorh/'
      }
    };
  },
  props: {
    canPaste: {
      type: Boolean,
      default() {
        return true;
      }
    },
    value: {
      type: String,
      default: ''
    },
    config: {
      type: Object,
      default: function() {
        return {};
      }
    },
    height: {
      type: String
    },
    width: {
      type: String
    },
    toolbar: {
      type: Array,
      default() {
        return [];
      }
    },
    disabled: {
      type: Boolean,
      defualt() {
        return false;
      }
    }
  },
  components: {},
  computed: {
    mixedConfig() {
      return Object.assign({}, this.defaultConfig, this.config);
    },
    style() {
      let style = {};

      if (this.height) {
        style.height = this.height;
      }

      if (this.width) {
        style.width = this.width;
      }
      return style;
    }
  },
  methods: {
    // 实例化编辑器
    initEditor() {
      this.$nextTick(() => {
        this.editor = window.UE.getEditor(this.id, this.mixedConfig);
        this.editor.addListener('ready', () => {
          this.status = 2;
          this.editor.setContent(this.initValue);
          this.$emit('ready', this.editor);
          this.editor.addListener('contentChange', () => {
            this.$emit('input', this.editor.getContent());
          });
          this.editor.addListener('beforepaste', async (o, html) => {
            if (!this.canPaste) {
              html.html = '';
            }
            setTimeout(() => {
              this.getImgList();
            }, 50);
          });
        });
      });
    },
    // 检测依赖,确保 UEditor 资源文件已加载完毕
    checkDependencies() {
      return new Promise((resolve, reject) => {
        // 判断ueditor.config.js和ueditor.all.js是否均已加载(仅加载完ueditor.config.js时UE对象和UEDITOR_CONFIG对象存在,仅加载完ueditor.all.js时UEDITOR_CONFIG对象存在,但为空对象)
        let scriptsLoaded =
          !!window.UE && !!window.UEDITOR_CONFIG && Object.keys(window.UEDITOR_CONFIG).length !== 0 && !!window.UE.getEditor;
        if (scriptsLoaded) {
          resolve();
        } else if (window.loadEnv) {
          // 利用订阅发布，确保同时渲染多个组件时，不会重复创建script标签
          window.loadEnv.on('scriptsLoaded', () => {
            resolve();
          });
        } else {
          window.loadEnv = new LoadEvent();
          // 如果在其他地方只引用ueditor.all.min.js，在加载ueditor.config.js之后仍需要重新加载ueditor.all.min.js，所以必须确保ueditor.config.js已加载
          this.loadConfig()
            .then(() => this.loadCore())
            .then(() => {
              resolve();
              window.loadEnv.emit('scriptsLoaded');
            });
        }
      });
    },
    loadConfig() {
      return new Promise((resolve, reject) => {
        if (window.UE && window.UEDITOR_CONFIG && Object.keys(window.UEDITOR_CONFIG).length !== 0) {
          resolve();
          return;
        }
        let configScript = document.createElement('script');
        configScript.type = 'text/javascript';
        configScript.src = this.mixedConfig.UEDITOR_HOME_URL + 'ueditor.config.js';
        document.getElementsByTagName('head')[0].appendChild(configScript);
        configScript.onload = function() {
          if (window.UE && window.UEDITOR_CONFIG && Object.keys(window.UEDITOR_CONFIG).length !== 0) {
            resolve();
          } else {
            console.error('加载ueditor.config.js失败,请检查您的配置地址UEDITOR_HOME_URL填写是否正确!\n', configScript.src);
          }
        };
      });
    },
    loadCore() {
      return new Promise((resolve, reject) => {
        if (window.UE && window.UE.getEditor) {
          resolve();
          return;
        }
        let coreScript = document.createElement('script');
        coreScript.type = 'text/javascript';
        coreScript.src = this.mixedConfig.UEDITOR_HOME_URL + 'ueditor.all.min.js';
        document.getElementsByTagName('head')[0].appendChild(coreScript);
        coreScript.onload = function() {
          if (window.UE && window.UE.getEditor) {
            resolve();
          } else {
            console.error('加载ueditor.all.min.js失败,请检查您的配置地址UEDITOR_HOME_URL填写是否正确!\n', coreScript.src);
          }
        };
      });
    },
    // 设置内容，这里很好，比较了ueditor的事件和初始化时候的变化
    setContent(value) {
      value === this.editor.getContent() || this.editor.setContent(value);
    },
    getImgList() {
      let str = this.editor.getContent();
      let imgReg = /<img[^>]* src=['"](http[s]*:\/\/[^'"]+)[^>]*>/g;
      this.imgList = str.match(imgReg) || [];
    },
    setNewImg(img) {
      let url = img.match(/src=['"]?([^'"]*)['"]?/i)[1];
      url = url.replace(/(\?|#)[^'"]*/, '');
      let params = {
        url: url
      };
      http.post('/admin/file/downloadFromNet.jhtml', params).then(data => {
        // data.path = '/upload/image/201901/4652e4d0-e5db-4fea-81f7-29d263e23271.jpg';

        let content = this.editor.getContent();
        content = content.replace(img, `<img src="${data.path}" style="max-width: 100%;">`);
        this.$emit('input', content);
      });
    }
  },
  beforeDestroy() {
    if (this.editor && this.editor.destroy) {
      this.editor.destroy();
    }
  },
  // v-model语法糖实现，这块太棒了啊
  watch: {
    value: {
      handler(value) {
        // 0: 尚未初始化 1: 开始初始化但尚未ready 2 初始化完成并已ready
        switch (this.status) {
          case 0:
            this.status = 1;
            this.initValue = value;
            this.checkDependencies().then(() => this.initEditor());
            break;
          case 1:
            this.initValue = value;
            break;
          case 2:
            this.setContent(value);
            setTimeout(() => {
              this.getImgList();
            }, 50);
            break;
          default:
            break;
        }
      },
      immediate: true
    },
    imgList(imgList) {
      for (const img of imgList) {
        this.setNewImg(img);
      }
    },
    disabled(val) {
      if (val) {
        this.editor.setDisabled();
      } else {
        this.editor.setEnabled();
      }
    }
  }
};
</script>


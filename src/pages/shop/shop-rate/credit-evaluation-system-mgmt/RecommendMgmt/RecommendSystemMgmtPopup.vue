<template>
  <biz-popup-tabs
    :open="open"
    v-model="tabSelection"
    @close="close"
    >
    <el-tab-pane label="推荐规则" name="0">
      <recommend-rule
        :itemId="itemId"
        @operationSuccess="operationSuccess"
      ></recommend-rule>
    </el-tab-pane>
  </biz-popup-tabs>
</template>

<script>
import { BizPopupTabs } from '@/components/BizPopup';

import RecommendRule from './RecommendRule';
// import RecommendRule from '../../shop-evaluation/ShopEvaluation'; // 调试用

export default {
  name: 'RecommendSystemMgmtPopup',
  components: {
    BizPopupTabs,
    RecommendRule
  },
  props: {
    open: {
      type: Boolean
    },
    itemId: {
      type: Object,
      default () {
        return {
          id: ''
        };
      }
    }
  },
  data () {
    return {
      tabSelection: '0'
    };
  },
  methods: {
    close (e) {
      this.$emit('close', e);
    },
    operationSuccess (e) {
      this.$emit('operationSuccess', e);
    }
  },
  watch: {
    open (val) {
      if (val) {
        setTimeout(() => {
          this.tabSelection = '0';
        }, 500);
      }
    }
  }
};
</script>

